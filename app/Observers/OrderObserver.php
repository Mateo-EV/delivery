    /**
     * Handle the Order "created" event.
     *
     * @param  \App\Models\Order  $order
     * @return void
     */
    public function created(Order $order)
    {
        $lastOrder = Order::orderBy('created_at')->take(1)->get();
        $code = intval($lastOrder->code);
        $code = strval(++$code);
        $order->code = $code;
        $order->save();
    }

